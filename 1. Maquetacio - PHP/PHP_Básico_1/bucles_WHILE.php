<?php

// WHILE --> Ejecuta código MIENTRAS se cumpla cierta condición 

$i = 0;

while ($i <= 10) {
    
    echo "Número $i <br>";
    $i ++;

}

echo "<br>";

// DO_WHILE --> Ejecuta (DO) y luego verifica la condición

$a = 1;

do {
    
    echo "Número $a <br>";
    $a++;

} while ($a <= 10);

?>